/**
 * op_add - returns the addition of a and b
 * @a: the first number to add
 * @b: the second number to add
 * Return: the addition of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns a minus b
 * @a: the first number
 * @b: the number to subtract from a
 * Return: a minus b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a times b
 * @a: the first number to multiply
 * @b: the multiplier
 * Return: a times b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns a divided by b
 * @a: the number to be divided
 * @b: the divisor
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns a modulus b
 * @a: the number to be divided
 * @b: the divisor
 * Return: a modulus b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
